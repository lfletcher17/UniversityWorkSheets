/** 
 * An interface Sortable which contains the method compareValue. The method 
 * compareValue is implemented within any class implementing this interface,
 * returning a double as specified within a given implementation.
 * @author lxf736
 * @version 2017-11-14 
 *
 */

public interface Sortable {
	
    /**
     *  @return a value of type double as specified within implementing class.
     */
	double compareValue();

}
